<?php

namespace Popsorin\Entity;

/**
 * Class HasId
 * @package Popsorin\Entity
 */
class HasId
{
    /**
     * @var int
     */
    private $id;

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     */
    public function setId($id)
    {
        $this->id = $id;
    }
}
